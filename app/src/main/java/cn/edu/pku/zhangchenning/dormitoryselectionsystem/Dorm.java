public class Dorm {
    private int wu;
    private int shisan;
    private int shisi;
    private int ba;
    private int jiu;

    public int getWu() {
        return wu;
    }

    public void setWu(int wu) {
        this.wu = wu;
    }

    public int getShisan() {
        return shisan;
    }

    public void setShisan(int shisan) {
        this.shisan = shisan;
    }

    public int getShisi() {
        return shisi;
    }

    public void setShisi(int shisi) {
        this.shisi = shisi;
    }

    public int getBa() {
        return ba;
    }

    public void setBa(int ba) {
        this.ba = ba;
    }

    public int getJiu() {
        return jiu;
    }

    public void setJiu(int jiu) {
        this.jiu = jiu;
    }
}
